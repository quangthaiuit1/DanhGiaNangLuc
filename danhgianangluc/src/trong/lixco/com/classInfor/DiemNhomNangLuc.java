package trong.lixco.com.classInfor;

public class DiemNhomNangLuc {
	String manhom;
	int diem=0;
	public DiemNhomNangLuc(String manhom, int diem) {
		super();
		this.manhom = manhom;
		this.diem = diem;
	}
	public String getManhom() {
		return manhom;
	}
	public void setManhom(String manhom) {
		this.manhom = manhom;
	}
	public int getDiem() {
		return diem;
	}
	public void setDiem(int diem) {
		this.diem = diem;
	}

	
}
